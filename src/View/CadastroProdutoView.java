    private void BotaoCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoCadastrarActionPerformed

        if(ValidarFormulario()){
            Produto produto = produtoController.salvar(
                    CampoNome.getText(),
                    Integer.parseInt(CampoQuantidade.getText()),
                    Double.parseDouble(CampoValor.getText()),
                    CampoCategoria.getText(),CampoMarca.getText()
            );
            
            if(produto.getIdProduto() > 0){
                this.Recarregar();
                JOptionPane.showMessageDialog(null,"Produto cadastrado com sucesso!");
                LimpaForm();
            } else {
            JOptionPane.showMessageDialog(null,"Falha ao cadastrar Produto!");
            }
        }
             

    }//GEN-LAST:event_BotaoCadastrarActionPerformed

    
    private void Recarregar(){
        
        ArrayList<String[]> linhasProdutos = produtoController.getProdutos();
        
        DefaultTableModel tmProdutos = new DefaultTableModel();
        tmProdutos.addColumn("Id");
        tmProdutos.addColumn("Nome");
        tmProdutos.addColumn("Quantidade");
        tmProdutos.addColumn("Valor");
        tmProdutos.addColumn("Categoria");
        tmProdutos.addColumn("Marca");
        TabelaProdutos.setModel(tmProdutos);        
        
                for(String[] c:linhasProdutos)
        {
            tmProdutos.addRow(c);
        }
        
    }
    
    private void LimpaForm(){
    CampoNome.setText("");
    CampoQuantidade.setText("");
    CampoValor.setText("");
    CampoCategoria.setText("");
    CampoMarca.setText("");
       
    }
    
    
